class DistanceConvertor{

      public static void main(String[] args){

             float distanceInKm = 10.8;

             // Conversion of Km to Miles by multiplying by 1.6
             float distanceInMiles = distanceInKm * 1.6;

             //Final output  in Miles
             System.out.println( " The distance "+  distanceInKm + " km in miles is "+  distanceInMiles );
      }
}
